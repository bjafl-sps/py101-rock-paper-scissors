# Oppgave 1: Lag variabler
mitt_navn = "Ole"
min_alder = 25
klar_til_spill = True

# Oppgave 2: Lag liste med spillvalg
spillvalg = ["stein", "saks", "papir"]

# Oppgave 3: Skriv ut alle variabler
print("Navn:", mitt_navn)
print("Alder:", min_alder)
print("Klar til å spille?", klar_til_spill)
print("Mulige valg:", spillvalg)

# Oppgave 4: String-operasjoner på navnet
print("Store bokstaver:", mitt_navn.upper())
print("Små bokstaver:", mitt_navn.lower())
print("Lengde på navn:", len(mitt_navn))
print("Første bokstav:", mitt_navn[0])

# Oppgave 5: Lage og endre poengvariabel
poeng = 0
print("Startpoeng:", poeng)
poeng += 1
print("Etter +1:", poeng)
poeng *= 2
print("Etter *2:", poeng)